package com.unicom.mms.cms.vo;

import java.io.Serializable;
import java.util.Date;

/**
 * 
 * 功能描述:策略vo
 * 
 * @author chenliang 新增日期：2013-6-27
 * @since mms-cms
 */
public class PolicEntity implements Serializable {

	private String startSendDate;
	private String startTime;
	private String endSendDate;
	private String endTime;

	public String getStartSendDate() {
		return startSendDate;
	}

	public void setStartSendDate(String startSendDate) {
		this.startSendDate = startSendDate;
	}

	public String getStartTime() {
		return startTime;
	}

	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}

	public String getEndSendDate() {
		return endSendDate;
	}

	public void setEndSendDate(String endSendDate) {
		this.endSendDate = endSendDate;
	}

	public String getEndTime() {
		return endTime;
	}

	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}

	@Override
	public String toString() {
		return "PolicEntity [startSendDate=" + startSendDate + ", startTime="
				+ startTime + ", endSendDate=" + endSendDate + ", endTime="
				+ endTime + "]";
	}

}
